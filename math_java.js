var myimg1 = document.getElementById("answer1");
var mybutton1 = document.getElementById("button1");

myimg1.style.display = "none";

mybutton1.addEventListener("click", event => {

    if (myimg1.style.display === "none") {
        myimg1.style.display = "block";
        mybutton1.textContent = "정답 가리기";
    }

    else {
        myimg1.style.display = "none";
        mybutton1.textContent = "정답 보기";

    }
});

